function authorBornIn1947() {
  const author = books.find((born) => born.author.birthYear === 1947);
  return author.author.name;
}

function smallerName() {
  let nameBook = books[0].name;
  books.forEach((smaller) => {
    if (smaller.name.length < nameBook.length) {
      nameBook = smaller.name;
    }
  });
  return nameBook;
}

function fantasyOrScienceFiction() {
  const genres = books.filter((elem) => elem.genre === 'Ficção Científica' || elem.genre === 'Fantasia');
  return genres;
  // Crie um array com todos os objetos que possuem gênero ficção científica ou fantasia.
}  

function getNamedBook() {
  const namedBook = books.find((nome) => nome.name.length === 26);
  return namedBook;
  // Encontre o livro cujo nome possui 26 caracteres.
}

function booksOrderedByReleaseYearDesc() {
  return books.sort(function(a, b){ return b.releaseYear - a.releaseYear });
  // Ordene os livros por data de lançamento em ordem decrescente.
}

